#include <stdio.h>

int main() {
	unsigned char chars[] = { 0xAC, 0xF3, 0x0C, 0x25, 0xA3, 0x10, 0xB7, 0x25, 0x16, 0xC6, 0xB7, 0xBC, 0x07, 0x25, 0x02, 0xD5, 0xC6, 0x11, 0x07, 0xC5, 0x00 };

	for (int k = 0; k < 21; k++) {
		for (int i = 0; i < 256; i++) {
			if (((i * 0xFB) & 0xFF) == chars[k]) {
				printf("%c", i);
			}
		}
	}
	return 0;
}